                    <h2>Untergrundvorbereitung beim Tapezieren</h2>

                    <p>Der Untergrund entscheidet über die Haltbarkeit jeder Tapete. Eine frisch gespachtelte Wand saugt Wasser aus dem Kleister so schnell auf, dass die Tapete kaum Zeit hat, sich anzulegen – das führt zu Blasen und Ablösungen. Eine ungereinigte Wand mit Fettflecken oder Altanstrichen, die nicht haften, gibt die Haftungsebene irgendwann auf – und die Tapete kommt gleich mit. Beides ist vermeidbar, wenn man die richtigen Vorbereitungsschritte kennt und konsequent durchführt.</p>

                    <p>Alte Tapeten werden zuerst entfernt – ob das einfach oder aufwändig ist, hängt von der Anzahl der Lagen und dem Untergrund darunter ab. Anschließend werden Risse und Fehlstellen mit Spachtelmasse geschlossen und die Oberfläche ggf. leicht angeschliffen. Dann folgt das Tapeziergrundieren oder Einsumpfen der Wand mit Tiefengrund: Dieser Schritt kontrolliert die Saugfähigkeit des Untergrunds, sorgt für eine gleichmäßige Haftfläche und gibt dem Kleber Zeit, die Tapete sicher zu befestigen. Das Überspringen dieses Schritts ist der häufigste Fehler beim Heimwerkertapezieren.</p>

                    <h2>Präzision beim Tapezieren – worauf es ankommt</h2>

                    <p>Bevor die erste Bahn hängt, werden Raumhöhe und Tapetenverbrauch berechnet. Als Faustregel gilt ein Mehrbedarf von zehn bis fünfzehn Prozent gegenüber der reinen Wandfläche – bei Mustertapeten mehr, da jede Bahn an den Rapport angepasst werden muss und dadurch mehr Verschnitt entsteht. Wir berechnen den Bedarf für Sie, damit Sie weder zu wenig noch zu viel kaufen.</p>

                    <p>Die erste Bahn wird stets an einem Lot (Senklot oder Laserlot) ausgerichtet, denn Wände sind selten wirklich senkrecht und Ecken selten wirklich im rechten Winkel. Die übrigen Bahnen werden stumpf gestoßen – Überlappungen sind bei modernen Tapeten nicht erwünscht, weil sie als dicker Wulst sichtbar bleiben. Bei Mustertapeten muss der Rapport auf Augenhöhe stimmen, da abgeschnittene Muster an der Decke oder am Boden deutlich weniger auffallen als in der Blickmitte. Innen- und Außenecken erfordern eigene Techniken, damit die Tapete ohne Falten in die Ecke geht und an Außenecken nicht aufreißt.</p>

                    <h2>Alte Tapeten entfernen – manchmal aufwändig</h2>

