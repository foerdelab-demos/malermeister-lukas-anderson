                    <h2>Welche Farben und Materialien setzen wir ein?</h2>

                    <p>Die Auswahl der richtigen Farbe richtet sich nach dem Raumtyp, dem Untergrund und den Anforderungen des Auftraggebers. Für Wohnräume verwenden wir bevorzugt Dispersionsfarben hochwertiger Hersteller wie Caparol, Alpina oder DAW – Produkte, die sich in der Praxis über viele Jahre bewährt haben und eine gute Deckkraft bei angenehmer Verarbeitung bieten. Die Deckkraftklassen reichen von 1 bis 4; wir empfehlen für Neuanstriche und Farbwechsel in der Regel Klasse 1 oder 2.</p>

                    <p>In Feuchträumen wie Bädern und Küchen kommen Farben mit fungiziden Zusätzen zum Einsatz, die Schimmelbildung hemmen. Alternativ eignen sich Silikatfarben, die diffusionsoffen sind und durch ihren hohen pH-Wert von Natur aus wenig Angriffsfläche für Schimmelsporen bieten. Für Haushalte mit Kleinkindern oder Allergikern führen wir auch emissionsarme Farben mit niedrigem VOC-Gehalt, die nach dem Streichen schnell austrocknen und kaum Geruch entwickeln. Das Gespräch über Materialauswahl gehört für uns zur Beratungsleistung, bevor überhaupt ein Angebot entsteht.</p>

