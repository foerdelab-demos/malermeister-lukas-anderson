                    <h2>Häufige Fragen zur Innenmalerei</h2>

                    <h3>Wie lange dauert das Streichen eines Zimmers?</h3>
                    <p>Die Dauer hängt stark vom Zustand des Untergrunds ab. Ein gut vorbereitetes Zimmer mit rund 20 Quadratmetern Wandfläche lässt sich einschließlich Voranstrich und Schlussanstrich in ein bis zwei Tagen fertigstellen. Wenn umfangreiche Spachtelarbeiten nötig sind oder mehrere Schichten Farbe gebrochen werden müssen, verlängert sich die Zeit entsprechend. Wir nennen Ihnen vor Beginn eine realistische Einschätzung, damit Sie Ihre eigene Planung darauf abstimmen können.</p>

                    <h3>Muss ich die Möbel ausräumen?</h3>
                    <p>Wir schützen vorhandene Möbel mit Abdeckvlies und Folie, sodass ein vollständiges Ausräumen in den meisten Fällen nicht notwendig ist. Empfehlenswert ist es dennoch, zerbrechliche Gegenstände, Dekorationen und persönliche Wertgegenstände vorab beiseite zu räumen. Schwere Möbelstücke werden von uns vorsichtig von der Wand gerückt, gearbeitet und anschließend wieder an ihre ursprüngliche Position gestellt.</p>

                    <h3>Wie viele Anstriche sind nötig?</h3>
                    <p>In der Regel sind zwei Anstriche erforderlich: ein Voranstrich zur Vorbereitung des Untergrunds und ein Schlussanstrich für die fertige Oberfläche. Bei einem Farbwechsel von einem dunklen zu einem hellen Ton kann ein dritter Auftrag notwendig werden, damit die alte Farbe nicht mehr durchscheint. Ob ein oder zwei Schlussanstriche benötigt werden, hängt auch von der Deckkraft der gewählten Farbe und der Saugfähigkeit des Untergrunds ab – das klären wir bei der Besichtigung.</p>

