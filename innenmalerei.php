                    <h2>Unser Vorgehen – Schritt für Schritt</h2>

                    <p>Jede Innenmalerarbeit folgt einem klaren Ablauf, der sicherstellt, dass das Ergebnis nicht nur beim ersten Anblick überzeugt, sondern auch dauerhaft hält. Die einzelnen Schritte bauen aufeinander auf und dürfen nicht vertauscht oder ausgelassen werden.</p>

                    <ol class="process-steps">
                        <li class="process-step">
                            <strong>Besichtigung und Aufmaß:</strong> Bevor ein Pinsel angesetzt wird, besichtigen wir die Räume gemeinsam mit Ihnen. Dabei beurteilen wir den Zustand des Untergrunds, notieren vorhandene Schäden wie Risse, Feuchtigkeitsflecken oder alte Tapetenschichten und nehmen das Aufmaß auf. Auf dieser Grundlage erstellen wir ein genaues Angebot ohne versteckte Nachträge.
                        </li>
                        <li class="process-step">
                            <strong>Untergrundvorbereitung:</strong> Risse werden mit geeigneter Spachtelmasse geschlossen, alte und lose Farbe abgeschliffen oder abgezogen. Saugfähige oder ungleichmäßige Untergründe erhalten eine Tiefengrundierung, damit die Deckfarbe gleichmäßig haftet und nicht ungleichmäßig einzieht. Dieser Schritt bestimmt maßgeblich die Qualität des Endresultats.
                        </li>
