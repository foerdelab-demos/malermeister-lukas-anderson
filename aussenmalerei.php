                    <h2>Welche Farben für den Außenbereich?</h2>

                    <p>Für Holzfenster und Holzverkleidungen verwenden wir Alkydharzmischungen oder moderne Wasserlacke mit UV-Schutz. Holzschutzlasuren – sowohl halbtransparent als auch deckend – bieten bei geeignetem Untergrund und regelmäßiger Pflege ausgezeichneten Schutz und lassen die Holzstruktur sichtbar oder betonen sie. Der Unterschied zwischen einer deckenden und einer halbtransparenten Lasur liegt in der Pflegefreundlichkeit: Deckende Systeme schützen stärker, sind aber bei späteren Renovierungen aufwändiger abzutragen.</p>

                    <p>Für Metallflächen kommen je nach Anforderung Rostschutzfarben, Kunstharzlacke oder Hammerschlaglacke zum Einsatz. Letztere sind für sichtbare Metallelemente wie Geländer oder Tore besonders geeignet, da ihre strukturierte Oberfläche kleine Unebenheiten im Untergrund kaschiert und gleichzeitig robust gegen mechanische Beanspruchung ist. Wir arbeiten mit bewährten Produkten von Herstellern wie Brillux, Sikkens (Akzo Nobel) und Caparol, die für den gewerblichen Handwerk gedacht sind und keine Kompromisse bei der Qualität eingehen.</p>

                    <h2>Wann ist der richtige Zeitpunkt?</h2>

                    <p>Außenanstriche setzen eine Mindestemperatur von fünf Grad Celsius voraus – sowohl bei der Verarbeitung als auch in den Stunden danach, bis die Farbe angetrocknet ist. Frost in der Nacht nach dem Streichen kann einen frischen Anstrich zerstören. Regen während der Verarbeitungszeit oder kurz danach wäscht wasserlösliche Produkte ab und lässt Haftbrücken versagen. Wir planen unsere Außenarbeiten daher sorgfältig im Voraus und richten uns nach dem Wetterbericht – auch wenn das bedeutet, einen Termin kurzfristig zu verschieben.</p>

                    <p>Die besten Jahreszeiten für Außenanstriche sind das späte Frühjahr, der Frühsommer und der frühe Herbst. Im Hochsommer ist direktes Sonnenlicht auf heißen Oberflächen zu vermeiden, da es die Trocknung zu schnell vorantreibt und Lackfilme durch Blasen und Risse versagen können. Wir stimmen die Tagesplanung auf die Himmelsrichtung der zu streichenden Flächen ab, damit wir immer im optimalen Fenster arbeiten.</p>

