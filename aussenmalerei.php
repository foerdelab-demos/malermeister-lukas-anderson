                    <h2>Häufige Fragen</h2>

                    <h3>Wie lange hält ein Außenanstrich?</h3>
                    <p>Die Haltbarkeit hängt von der Sonnenexposition, der Qualität des Untergrunds und der verwendeten Produkte ab. Gut vorbereitete und fachgerecht gestrichene Holzfenster können acht bis fünfzehn Jahre halten, bevor eine vollständige Auffrischung notwendig wird. Regelmäßige Sichtprüfung alle zwei bis drei Jahre und kleine Ausbesserungen an kritischen Stellen – Schnittkanten, Glasleisten, Dichtungsanschlüsse – verlängern die Lebensdauer des Anstrichs deutlich. Wir empfehlen Ihnen bei der Übergabe, worauf Sie in den Folgejahren achten sollten.</p>

                    <h3>Können alte Anstriche überstrichen werden?</h3>
