                    <h2>Vorbereitung ist das Fundament – Untergrundarbeiten</h2>

                    <p>Erfahrene Handwerker wissen: Die Untergrundvorbereitung macht mindestens sechzig Prozent der Gesamtarbeit aus. Bevor Farbe aufgetragen wird, muss der bestehende Anstrich auf seine Haftung geprüft werden – mit dem Kreuzschnitttest oder durch gezielte Prüfung an kritischen Stellen. Loser oder abplatzender Altanstrich wird abgeschabt, abgeschliffen oder – bei Holz – sofern nötig mit einem Heißluftgebläse abgezogen, ohne das Holz selbst zu beschädigen.</p>

